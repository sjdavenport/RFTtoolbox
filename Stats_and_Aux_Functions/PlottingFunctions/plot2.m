function plot2( x )
% plot2
%--------------------------------------------------------------------------
% ARGUMENTS
% x: a 2D array
%--------------------------------------------------------------------------
% OUTPUT
% 
%--------------------------------------------------------------------------
% EXAMPLES
% x = randn(100,2)
% plot2(x)
%--------------------------------------------------------------------------
% AUTHOR: Samuel Davenport
%--------------------------------------------------------------------------

%%  Check mandatory input and get important constants
%--------------------------------------------------------------------------

%%  Main Function Loop
%--------------------------------------------------------------------------
s_x = size(x);
if s_x(1) > s_x(2)
    plot(x(:,1), x(:,2),'o');
else
    plot(x(1,:), x(2,:),'o');
end

end

