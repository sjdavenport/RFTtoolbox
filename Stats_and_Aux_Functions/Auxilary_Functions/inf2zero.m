function vec = inf2zero( vec )
% INF2NAN(vec) sets all of the nans to zeros.
%--------------------------------------------------------------------------
% ARGUMENTS
% vec       A vector.
%--------------------------------------------------------------------------
% OUTPUT
% vec       0/1
%--------------------------------------------------------------------------
% EXAMPLES
% nan2zero([1,5.1,5, Inf, 0.0001])
%--------------------------------------------------------------------------
% AUTHOR: Samuel Davenport
%--------------------------------------------------------------------------
vec(isinf(vec)) = 0;

end

